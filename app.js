function marioAnimation(){
    frameCanvas()
    let delayAcc = 0;
    for(i=0; i<10; i++){
        window.setTimeout(frameOne,delayAcc);
        window.setTimeout(frameTwo,delayAcc+500);
        window.setTimeout(frameOne,delayAcc+1000);
        window.setTimeout(frameThree,delayAcc+1500);
        delayAcc = delayAcc+2000
    }
}

