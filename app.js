function frameCanvas() {
    for(i=0; i<256; i++) {
        let animationTarget = document.getElementById("animation");
        let newDiv = document.createElement("div");
        // newDiv.id = i
        newDiv.classList.add("pixel-block")
        newDiv.style.backgroundColor = "white"
        animationTarget.appendChild(newDiv)
    }
}

var frameOne = function() {
    for(i=0; i<256; i++) {
        let pixelBlock = document.getElementsByClassName("pixel-block")[i]
        let colorVar = runningFirst[i]
        pixelBlock.style.backgroundColor = colorVar
    }
}

